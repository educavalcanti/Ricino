    <h1>Governança</h1>
    <h2>Composição</h2>
    <h3>1 - A Rede será formada inicialmente por três núcleos temáticos e dois regionais:</h3>
    <ol>
    <li>CRNO – Núcleo de Tecnologia da Construção e Reparação Naval e Offshore</li>
    <li>NPNO – Núcleo de Projeto de Embarcações e Sistemas Offshore</li>
    <li>NCP – Núcleo de Cadeia Produtiva da Indústria Naval e Offshore</li>
    <li>NRN – Núcleo Regional Nordeste–Norte</li>
    <li>NRS – Núcleo Regional Sul</li>
    </ol>
    <br />
    <h2>Os Núcleos</h2><p>
    <p>2 - Cada Núcleo será dirigido por um Comitê Gestor.
    <p>3 - O número de membros dos Comitês Gestores dos Núcleos estará entre 9 e 18. Os membros indicarão 1 titular e 1 suplente.
    <p>4 - O CEENO indicará 1/3 dos membros, entre representantes de instituições de pesquisa, com atuação no setor (por exemplo: universidades, centros de pesquisa, escolas técnicas e profissionais).
    <p>5 - O SINAVAL e o SYNDARMA indicarão 1/3 dos membros, entre representantes de associações/empresas ligadas à cadeia da indústria marítima, que demonstrem interesse efetivo nas atividades de P, D & I (por exemplo: estaleiros de construção e reparação, Armadores, Marinha do Brasil, fornecedores da indústria naval e offshore).
    <p>6 - A SOBENA, ouvidos o SINAVAL, SYNDARMA e CEENO, indicará 1/3 dos membros, entre representantes de associações ou sociedades representativas de segmentos relevantes da indústria ou da comunidade técnica, profissional ou científica; ou entre representantes de organizações, de qualquer nível de governo, envolvidas no processo de formulação ou execução de políticas tecnológicas ou setoriais para a indústria naval, em nível nacional ou regional (por exemplo: empresas de engenharia, classificadoras, Entidades Governamentais, Instituições Financeiras e de Fomento).
    <p>7 - O Coordenador Geral de cada Núcleo será indicado por maioria simples do respectivo Comitê Gestor.


    <br /><br />
    <h2>Gestão da REDE</h2><p>
    <p>8 - A direção geral da REDE cabe a um Comitê Gestor da REDE.</p>
    <p>9 - O número de membros do Comitê Gestor da REDE está entre 10 e 15, incluindo os seguintes representantes. Os membros indicarão 1 titular e 1 suplente.</p>
    <p>a.Coordenadores dos Núcleos (5)</p>
    <p>b.Representante da SOBENA</p>
    <p>c.Representante do SINAVAL</p>
    <p>d.Representante do SYNDARMA</p>
    <p>e.Representante do CEENO</p>
    <p>f.Representante da BNDES</p>
    <p>g.Representante da ABDI</p>
    <p>h.Representante do MDIC</p>
    <p>i.Representante do PROMINP</p>
    <p>10 - Os demais dois membros possíveis são indicados pelos membros acima, caso acordado, e definidos por maioria simples.</p>
    <p>11 - O Coordenador Geral da REDE e seu suplente serão indicados por maioria simples do Comitê Gestor.</p>
    <p>12 - Todos os membros dos Comitês e os Coordenadores tem mandatos de 2 anos, permitida recondução.</p>
    <br />
    <h2>Atribuições</h2>
    <h3>13 - Cabe ao Comitê Gestor de Núcleo:</h3>
    <p>> a articulação entre associações, entidades e empresas para identificar as ações relevantes em sua área de atuação;</p>
    <p>> estabelecer, anualmente, o calendário geral das atividades do núcleo;</p>
    <p>> a preparação de uma carteira de projetos ou ações consideradas prioritárias, garantindo que todas as ações envolvam diretamente a indústria;</p>
    <p>> preparar um Plano Anual de Ações Estratégicas;</p>
    <p>> realizar seminários anuais de avaliação das ações em andamento;</p>
    <p>> divulgar as ações por meio do Portal da REDE;</p>
    <p>> revisar os planos e metas, em função da avaliação anual.</p>


    <h3>14 - Cabe ao Comitê Gestor da REDE:</h3>
    <p>> consolidar os Planos Anuais dos Núcleos em um Plano Nacional de Capacitação Tecnológica da Indústria Naval e Offshore;</p>
    <p>> realizar avaliação anual consolidada das ações;</p>
    <p>> implantar e manter o PORTAL da REDE, e realizar outras atividades de divulgação relevantes;</p>
    <p>> promover a articulação entre os segmentos da cadeia produtiva, instituições de pesquisa e instituições governamentais e não governamentais, nacionais e internacionais, visando à preparação do Plano Anual, e à avaliação anual;</p>
    <p>> promover as gestões relevantes no sentido de captar recursos, de fontes nacionais e internacionais, para financiamento do Plano Nacional;</p>
    <p>> realizar um seminário anual, para avaliação e divulgação das atividades da REDE.</p>

